                <p>Pomorie is located in the southern part of the Bulgarian Black Sea Coast, 22 km away from
                   Burgas and approximately 400 km away from Sofia. The population of the town is around 14 000
                   people. E87 main road connecting the northern and the southern parts of the Black Sea Coast
                   passes through Pomorie. Varna, Burgas, Sozopol, and Nesebar can be reached via Pomorie Port.
                   The town lies in close proximity to the international airport of Burgas.</p>

                <p>Pomorie is one of the most popular destinations in summer– the average air and water
                   temperature in the month of July is 23.6°С, there are up to 2360 hours of sunshine every
                   year, and the beach is about 5 km long. Winters are mild and with minimum precipitation.</p>
                <p>Moreover, the region has been famous for the medicinal qualities of the mud extracted during
                   the production of salt in Pomorie Lake ever since ancient times. The healing powers of the
                   mineral water in the town are also well-known. The slow cooling of the sea water is very
                   typical for the region. In the morning before sunrise the air is rich in ozone, negative
                   ions, ultraviolet rays and iodine released in gas form by seaweed.</p>
