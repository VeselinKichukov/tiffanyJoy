                                Bulgarian Black Sea Coast.</p>
                <p>It is situated in Burgas Province, 20 km away from the city of Burgas and 18 km from the
                   Sunny Beach resort. The ultrasaline lagoon Lake Pomorie, the northernmost of the Burgas
                   Lakes, lies in the immediate proximity. The town is the administrative centre of the
                   eponymous Pomorie Municipality.</p>
